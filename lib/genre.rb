require "pry"

class Genre
  extend Concerns::Findable
  
  attr_accessor :name 
  
  @@all = []
  
  def initialize(name)
    @name = name
    @songs = []
  end
  
  def self.all 
    @@all
  end
  
  def self.destroy_all
    @@all.clear
  end
  
  def save
    @@all << self
  end
  
  def self.create(name)
    song = self.new(name)
    song.save
    song
  end
  
  def songs
    @songs
  end
  
  def artists
    art_list = self.songs.collect do |song|
      song.artist
    end
    art_list.uniq
  end
end